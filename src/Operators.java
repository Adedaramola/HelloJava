public class Operators {
    public static void main(String[] args) {
        int num1 = 20, num2 = 30;

        // Addition of integers
        System.out.println(num1 + num2);
        // Subtraction
        System.out.println(num1 - num2);
        // Multiplication
        System.out.println(num1 * num2);
        // Pre-increment
        System.out.println(++num1);
        // Post-increment
        System.out.println(num1++);

        System.out.println(Math.pow(num1, num2));
    }
}
